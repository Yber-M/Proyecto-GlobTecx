package proyectFrmClass;

public class registro {

    String name;
    String tipPag;
    String salRuc; 
    String correO; 
    Double prcTt;
    String fchHr;

    public registro(String name, String tipPag, String salRuc, String correO, Double prcTt, String fchHr) {
        this.name = name;
        this.tipPag = tipPag;
        this.salRuc = salRuc;
        this.correO = correO;
        this.prcTt = prcTt;
        this.fchHr = fchHr;
    }
    
}
